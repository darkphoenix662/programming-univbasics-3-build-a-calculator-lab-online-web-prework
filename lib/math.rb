def addition(num2, num1)
  return (num2 + num1)
end

def subtraction(num2, num1)
  return (num2 - num1)
end

def division(num2, num1)
  return (num2 / num1)
end

def multiplication(num2, num1)
  return (num2 * num1)
end

def modulo(num2, num1)
  return (num2 % num1)
end

def square_root(num1)
  return Math.sqrt(num1)
end